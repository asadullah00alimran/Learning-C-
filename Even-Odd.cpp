#include<iostream>
using namespace std;

int main()
{
    int n;

    cout<<"Enter an integer number which you are want to check : ";
    cin>>n;

    if(n%2==0)
    {
        cout<<"\nThe number is Even\n";
    }

    else
    {
        cout<<"\nThe Number is Odd\n";
    }


    return 0;
}

